// Logic to check if two rows can be safely merged
const attemptMerge = (source: Row, target: Row): { success: boolean; mergedRow?: Row; error?: string } => {
  const merged: any = { Word: target.Word }
  const errors: string[] = []

  for (const key of DATA_KEYS) {
    const sVal = cleanVal(source[key])
    const tVal = cleanVal(target[key])

    if (sVal && tVal && sVal !== tVal) {
      errors.push(key) // Just push key name, we'll detail it in UI
    } else {
      merged[key] = tVal ?? sVal ?? null
    }
  }

  if (errors.length > 0) {
    return { success: false, error: errors.join(', ') }
  }

  return { success: true, mergedRow: merged as Row }
}

// --- 3. ANALYSIS ENGINE ---

interface AnalysisResult {
  source: Row

  // Strategy 1: Previous Lib
  prevLibWord: string
  prevLibStatus: 'SAME' | 'SAFE_RENAME' | 'SAFE_MERGE' | 'CLASH' | 'BATCH_CLASH'
  prevLibTarget?: Row | null // If clash/merge, store the target row
  prevLibErr?: string

  // Strategy 2: Khmer Normalizer
  khNormWord: string
  khNormStatus: 'SAME' | 'SAFE_RENAME' | 'SAFE_MERGE' | 'CLASH' | 'BATCH_CLASH'
  khNormTarget?: Row | null
  khNormErr?: string
}

const analyzeDB = (db: Database): AnalysisResult[] => {
  console.log(chalk.blue('üîç Analyzing Database...'))

  const existingWords = new Set<string>()
  db.query('SELECT Word FROM km_Dict')
    .all()
    .forEach((r: any) => existingWords.add(r.Word))

  const candidates = db
    .query(
      `
    SELECT * FROM km_Dict
    WHERE Desc IS NOT NULL
      AND Wiktionary IS NULL
      AND from_csv_variants IS NULL
      AND from_csv_nounForms IS NULL
      AND from_csv_pronunciations IS NULL
      AND from_csv_rawHtml IS NULL
      AND from_chuon_nath IS NULL
      AND from_chuon_nath_translated IS NULL
      AND from_russian_wiki IS NULL
  `,
    )
    .all() as Row[]

  const results: AnalysisResult[] = []

  const getStatus = (
    oldWord: string,
    newWord: string,
  ): {
    status: AnalysisResult['prevLibStatus']
    target?: Row | null
    err?: string
  } => {
    if (newWord === oldWord) return { status: 'SAME' }
    if (!isKhmerMalformed(oldWord)) return { status: 'SAME' }

    if (existingWords.has(newWord)) {
      const target = db.query('SELECT * FROM km_Dict WHERE Word = ?').get(newWord) as Row | null

      if (!target) return { status: 'BATCH_CLASH', err: 'Target created in current run context' }

      // Check Merge Safety
      const source = db.query('SELECT * FROM km_Dict WHERE Word = ?').get(oldWord) as Row
      const mergeCheck = attemptMerge(source, target)

      if (mergeCheck.success) return { status: 'SAFE_MERGE', target }
      return { status: 'CLASH', target, err: mergeCheck.error }
    }

    return { status: 'SAFE_RENAME' }
  }

  for (const row of candidates) {
    const oldWord = row.Word

    // 1. Calc Prev Lib
    let pWord = oldWord
    try {
      pWord = normalizePrevLib(oldWord)
    } catch (e) {}
    const pStat = getStatus(oldWord, pWord)

    // 2. Calc Khmer Normalizer
    let kWord = oldWord
    try {
      kWord = khnormal(oldWord)
    } catch (e) {}
    const kStat = getStatus(oldWord, kWord)

    if (pWord !== oldWord || kWord !== oldWord) {
      results.push({
        source: row,
        prevLibWord: pWord,
        prevLibStatus: pStat.status,
        prevLibTarget: pStat.target,
        prevLibErr: pStat.err,
        khNormWord: kWord,
        khNormStatus: kStat.status,
        khNormTarget: kStat.target,
        khNormErr: kStat.err,
      })
    }
  }

  console.log(chalk.green(`‚úÖ Analysis done. Found ${results.length} candidates.`))
  return results
}

// --- 4. ACTION HANDLER ---

const applyChange = (oldWord: string, newWord: string) => {
  const db = new Database(DICT_DB_PATH)
  try {
    const source = db.query('SELECT * FROM km_Dict WHERE Word = ?').get(oldWord) as Row | null
    if (!source) throw new Error('Source word no longer exists')

    const target = db.query('SELECT * FROM km_Dict WHERE Word = ?').get(newWord) as Row | null

    if (target) {
      const mergeRes = attemptMerge(source, target)
      if (!mergeRes.success) throw new Error(`Unsafe Merge: ${mergeRes.error}`)

      db.transaction(() => {
        db.query('DELETE FROM km_Dict WHERE Word = ?').run(newWord)
        const setClause = DATA_KEYS.map(k => `${k} = $${k}`).join(', ')
        const params: any = { $newWord: newWord, $oldWord: oldWord }
        DATA_KEYS.forEach(k => (params[`$${k}`] = mergeRes.mergedRow![k]))
        db.query(`UPDATE km_Dict SET Word = $newWord, ${setClause} WHERE Word = $oldWord`).run(params)
      })()
      return { msg: `Merged ${oldWord} -> ${newWord}` }
    } else {
      db.query('UPDATE km_Dict SET Word = ? WHERE Word = ?').run(newWord, oldWord)
      return { msg: `Renamed ${oldWord} -> ${newWord}` }
    }
  } finally {
    db.close()
  }
}

