// --- 2. HELPERS ---

const cleanVal = (v: any): string | null => (v === null || v === undefined || v === '' ? null : String(v))

const isKhmerMalformed = (text: string): { isMalformed: boolean; reason?: string } => {
  if (!text) return { isMalformed: false }

  // 1. Check for Vowel before Coeng (The issue with ·ûÑ·û∂·üí·û†·û∂·ûâ)
  // \u17B6-\u17C5 are Dependent Vowels. \u17D2 is Coeng.
  if (/[\u17B6-\u17C5]\u17D2/.test(text)) {
    return { isMalformed: true, reason: 'Vowel before Subscript (Coeng)' }
  }

  // 2. Check for Coeng followed by something that isn't a Consonant
  // Coeng (\u17D2) MUST be followed by a Consonant (\u1780-\u17A2)
  if (/\u17D2[^\u1780-\u17A2]/.test(text)) {
    return { isMalformed: true, reason: 'Subscript not followed by Consonant' }
  }

  // 3. Check for Coeng at the very end of the string
  if (/\u17D2$/.test(text)) {
    return { isMalformed: true, reason: 'Word ends with Coeng' }
  }

  // 4. Check for Double Vowels (e.g. ·û∂ followed by ·û∑)
  // This usually renders with a dotted circle or overlapping glyphs.
  if (/[\u17B6-\u17C5][\u17B6-\u17C5]/.test(text)) {
    return { isMalformed: true, reason: 'Double Vowels detected' }
  }

  // 5. Check for Diacritic before Vowel (Common typing error)
  // e.g. Bantoc (\u17CB) before Vowel usually renders wrong or is semantically wrong
  // Note: This rule is stricter; some fonts tolerate it, but it's technically wrong order.
  if (/[\u17C9-\u17D0][\u17B6-\u17C5]/.test(text)) {
    return { isMalformed: true, reason: 'Diacritic before Vowel' }
  }

  return { isMalformed: false }
}

const fixDeprecatedChars = (text: string): string => {
  if (!text) return ''
  return text.replace(/\u17A3/g, '\u17A2')
}

const normalizePrevLib = (word: string): string => {
  let processed = fixDeprecatedChars(word)
  processed = reorderText(processed)
  return processed.trim()
}

